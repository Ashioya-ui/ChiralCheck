"""
immune.py — Epistemic Immune System
by William Ashioya | SHOAL | github.com/Ashioya-ui/ChiralCheck

WHAT THIS IS:
  A runtime action gate for AI agents. Before any proposed action executes,
  it is routed to the appropriate epistemic detector based on risk level.
  If hallucination/incoherence is detected above threshold, the action is
  blocked, access is conditionally revoked, and state is rolled back.

THREE DETECTORS (grounded in published research):

  LOW risk  → Semantic Entropy  (Farquhar et al., Nature 2024)
    Sample K responses to the same prompt at non-zero temperature.
    Cluster by cosine similarity using Voyage embeddings.
    Compute Shannon entropy over the cluster distribution.
    High entropy = agent is semantically uncertain = likely confabulating.
    Black-box compatible: no token probabilities required.

  HIGH risk → Covariance Log-Det  (Chen et al., ICLR 2024 — INSIDE proxy)
    Generate K adversarial perturbations of the proposed action.
    Embed all of them and measure the log-det of the covariance matrix.
    Diffuse cloud = action plan has no stable semantic core = block it.
    NOTE: True EigenScore requires white-box hidden state access.
    This is the black-box proxy from ChiralCheck T2 — same principle,
    external embeddings instead of internal activations.

  REASONING risk → Effective Complexity proxy  (Gell-Mann & Lloyd, 2010)
    Pass the reasoning trace through score_utility() from axes.py.
    High isolation_index = claims float unsupported = incoherent reasoning.
    Not a Kolmogorov complexity calculation — a structural proxy.

INTEGRATION:
  Connects directly to providers.py, chiral.py, and axes.py.
  No standalone mocks. All detectors use real LLM + embedding calls.

RED TEAM FIXES vs the original prototype:
  - semantic entropy: string-length mock replaced with real embedding clusters
  - eigenscore: white-box stub replaced with covariance log-det (black-box)
  - effective_complexity: "synergy" heuristic replaced with utility isolation index
  - sampled_responses: generated by LLM at temperature, not passed pre-made
  - thresholds: documented with calibration notes, not raw guesses
  - rollback: honest about scope (in-memory only)
  - full integration with providers.py / axes.py / chiral.py
"""

